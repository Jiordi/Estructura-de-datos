    private boolean verificarTipo(String tipo) {
        return tipo.length() > 0;
    }

    private boolean verificarMarca(String marca) {
        return marca.length() > 0;
    }

    private boolean verificarModelo(String modelo) {
        return modelo.length() > 0;
    }

    private boolean verificarAño(int año) {
        return año >= 1900 && año <= 2019;
    }

    private boolean verificarColor(String color) {
        return color.length() > 0;
    }

    private boolean verificarPrecio(float precio) {
        return precio > 0;
    }

    private boolean datosValidos() {
        return verificarTipo(txtTipo.getText()) && verificarMarca(txtMarca.getText()) && verificarModelo(txtModelo.getText()) && verificarAño(Integer.parseInt(txtAño.getText())) && verificarColor(txtColor.getText()) && verificarPrecio(Float.parseFloat(txtPrecio.getText()));
    }

