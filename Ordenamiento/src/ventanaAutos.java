    private boolean rbtnSeleccionado() {
        if (rbtnAscendente.isSelected()) {
            posicion = 0;
        } else if (rbtnDescendiente.isSelected()) {
            posicion = 1;
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un radiobutton");
            return false;
        }
        return true;
    }

